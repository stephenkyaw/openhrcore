export function permissionMeta(permission) {
  const [feature, action, desc] = PERMISSION_META[permission] || [
    permission,
    permission,
    "Permission action",
  ];
  return { feature, action, desc };
}

export function rolePresetPermissions(preset) {
  if (preset === "employee") return ROLE_PERMS.r4 || [];
  if (preset === "manager") return ROLE_PERMS.r3 || [];
  if (preset === "finance") return ROLE_PERMS.r5 || [];
  if (preset === "hr-admin") return ROLE_PERMS.r2 || [];
  return [];
}
